    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String sql = "Select * from Issue where Student_Id = ?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1,jTextField1.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                String s = rs.getString("SName");
                jTextField2.setText(s);
                String s1 = rs.getString("Course");
                jTextField3.setText(s1);
                String s2 = rs.getString("Year");
                jTextField4.setText(s2);
                String s3 = rs.getString("Book_Id");
                jTextField5.setText(s3);
                String s4 = rs.getString("Name");
                jTextField6.setText(s4);
                String s5 = rs.getString("Price");
                jTextField7.setText(s5);
                String s6 = rs.getString("DateIssue");
                jTextField8.setText(s6);
                
                rs.close();
                pst.close();
            }else{
                JOptionPane.showMessageDialog(null,"Student id not found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

